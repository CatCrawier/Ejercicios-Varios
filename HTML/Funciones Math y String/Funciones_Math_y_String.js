// Funciones matemáticas en JavaScript

console.log("Resultados de redondeo: " + Math.round(2.95))
console.log("Resultados de redondeo hacia abajo: " + Math.floor(2.95))
console.log("Resultados de redondeo hacia arriba: " + Math.ceil(2.95))
console.log("Resultados de la función exponencial: " + Math.exp(2))
console.log("Resultados de la función logarítmica: " + Math.log(10))
console.log("Resultados de la función potencia: " + Math.pow(2,7))
console.log("Resultados de la función aleatoria: " + Math.random())
console.log("Resultados de la función aleatoria * 10: " + Math.random() * 10)
console.log("Resultados de la función aleatoria redondeada: " + Math.round(Math.random() * 10))
console.log("Resultados de la función mínimo: " + Math.min(5, 10, 15, 20))
console.log("Resultados de la función máximo: " + Math.max(5, 10, 15, 20))

